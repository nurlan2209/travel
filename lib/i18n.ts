import { DEFAULT_LANGUAGE, LANGUAGES, type AppLanguage } from "@/lib/constants";

export function normalizeLanguage(input?: string | null): AppLanguage {
  if (!input) return DEFAULT_LANGUAGE;
  const low = input.toLowerCase();
  if (LANGUAGES.includes(low as AppLanguage)) {
    return low as AppLanguage;
  }
  return DEFAULT_LANGUAGE;
}

export function mapLangToPrismaEnum(language: AppLanguage): "KZ" | "RU" | "EN" {
  if (language === "ru") return "RU";
  if (language === "en") return "EN";
  return "KZ";
}

